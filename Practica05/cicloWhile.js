//--------------- While ---------------\\
let contador =0;

while ( contador < 3 ) { //mientras la condiciÃ³n se cumpla el codigo se va a ejecutar
    
    console.log(contador);
    contador++;
     
}
//el valor final del contador es 3
console.log(contador);

//--------------- Do While ---------------\\
//se ejecuta al menos una vez el bloque interno del do while

contador=0;

do{
    console.log(contador);
    contador++;
}while( contador < 3 );

//De nuevo el contador es igual a 3
console.log(contador);

//Veremos cuando el contador es igual a tres

do{
    console.log(contador);
    contador++;
}while( contador < 3 );

//aqui vemos que se ejecuto de todas maneras y se incremento el contador
console.log(contador);






















